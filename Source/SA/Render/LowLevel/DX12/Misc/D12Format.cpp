	DXGI_FORMAT API_GetFormat(Format _format)
	{
		const uint32_t index = static_cast<uint32_t>(_format);

		SA_ASSERT((OutOfRange, index, 0u, sizeof(d12FormatIndexMap)), SA.Render.RHI.DX12,
			(L"Format value [%1] invalid", index));

		return d12FormatIndexMap[index];
	}
		
	Format API_GetFormat(DXGI_FORMAT _d12Format)
	{
		for (uint32_t i = 0; i < sizeof(d12FormatIndexMap); ++i)
		{
			if (d12FormatIndexMap[i] == _d12Format)
				return static_cast<Format>(i);
		}

		SA_LOG((L"DXGI_FORMAT [%1] not supported yet!", static_cast<uint32_t>(_d12Format)), Error, Sa.Render.RHI.DX12);

		return Format::R8G8B8A8_UNORM;
	}


	DXGI_FORMAT SRGBToUNORMFormat(DXGI_FORMAT _d12Format) noexcept
	{
		if (_d12Format == DXGI_FORMAT_R8G8B8A8_UNORM_SRGB)
			return DXGI_FORMAT_R8G8B8A8_UNORM;

		if (_d12Format == DXGI_FORMAT_B8G8R8A8_UNORM_SRGB)
			return DXGI_FORMAT_B8G8R8A8_UNORM;

		SA_LOG((L"D12Format [%1] not supported", _d12Format), Warning, SA.Render.DX12);

		return _d12Format;
	}

	DXGI_FORMAT UNORMToSRGBFormat(DXGI_FORMAT _d12Format) noexcept
	{
		if (_d12Format == DXGI_FORMAT_R8G8B8A8_UNORM)
			return DXGI_FORMAT_R8G8B8A8_UNORM_SRGB;

		if (_d12Format == DXGI_FORMAT_B8G8R8A8_UNORM)
			return DXGI_FORMAT_B8G8R8A8_UNORM_SRGB;

		SA_LOG((L"D12Format [%1] not supported", _d12Format), Warning, SA.Render.DX12);

		return _d12Format;
	}
	
	bool IsPresentFormat(DXGI_FORMAT _d12Format) noexcept
	{
		return _d12Format == DXGI_FORMAT_R8G8B8A8_UNORM_SRGB || _d12Format == DXGI_FORMAT_B8G8R8A8_UNORM_SRGB;
	}
